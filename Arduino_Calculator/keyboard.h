/**
 * Keyboard Input Handler for Arduino Calculator
 * 支持 5x5 矩阵键盘扫描，多模式按键复用
 */

#ifndef KEYBOARD_H
#define KEYBOARD_H

#include "config.h"

// ============================================================
// 按键事件类型
// ============================================================
enum KeyEvent {
  KEY_NONE = 0,
  KEY_PRESSED = 1,
  KEY_RELEASED = 2,
  KEY_HOLD = 3
};

// ============================================================
// 特殊按键定义
// ============================================================
#define KEY_CHAR_CLEAR    'C'
#define KEY_CHAR_DELETE   'D'
#define KEY_CHAR_SHIFT    'S'
#define KEY_CHAR_ALPHA    'A'
#define KEY_CHAR_MODE     'M'
#define KEY_CHAR_ENTER    'E'

// ============================================================
// 初始化键盘
// ============================================================
void keyboardInit();

// ============================================================
// 扫描键盘，返回按键字符
// 如果没有按键按下，返回 '\0'
// ============================================================
char keyboardScan();

// ============================================================
// 获取当前按键事件（带消抖）
// ============================================================
KeyEvent getKeyEvent(char& keyChar);

// ============================================================
// 等待按键按下（阻塞）
// ============================================================
char waitForKey();

// ============================================================
// 获取当前模式下的按键映射
// ============================================================
char getKeyForMode(char baseKey, CalcMode mode);

#endif // KEYBOARD_H
