/**
 * Display Manager for Arduino Calculator
 * 管理 LCD2004 显示，处理多行内容、滚动、模式指示等
 */

#ifndef DISPLAY_H
#define DISPLAY_H

#include "config.h"
#include <LiquidCrystal_I2C.h>
#include "expression_parser.h"

// ============================================================
// 显示区域定义
// ============================================================
#define DISP_ROW_INPUT    0   // 输入行
#define DISP_ROW_RESULT   1   // 结果行
#define DISP_ROW_STATUS   2   // 状态行（模式、角度）
#define DISP_ROW_MESSAGE  3   // 消息/错误行

// ============================================================
// 初始化显示
// ============================================================
void displayInit();

// ============================================================
// 清屏
// ============================================================
void displayClear();

// ============================================================
// 显示当前输入表达式
// ============================================================
void displayInput(const char* expr);

// ============================================================
// 显示计算结果
// ============================================================
void displayResult(const Value& value);

// ============================================================
// 显示状态信息（模式、角度制等）
// ============================================================
void displayStatus(CalcMode mode, AngleMode angleMode);

// ============================================================
// 显示错误信息
// ============================================================
void displayError(ErrorCode err);

// ============================================================
// 显示提示消息
// ============================================================
void displayMessage(const char* msg);

// ============================================================
// 显示矩阵内容（分页显示）
// ============================================================
void displayMatrix(const Matrix& mat, uint8_t page = 0);

// ============================================================
// 显示复数内容
// ============================================================
void displayComplex(const Complex& c);

// ============================================================
// 滚动显示长文本
// ============================================================
void displayScrollText(uint8_t row, const char* text);

// ============================================================
// 更新光标位置
// ============================================================
void displaySetCursor(uint8_t col, uint8_t row);

// ============================================================
// 背光控制
// ============================================================
void displayBacklight(bool on);

#endif // DISPLAY_H
