/**
 * Keyboard Input Handler for Arduino Calculator
 * 支持 4x4 矩阵键盘扫描，FN层切换
 */

#ifndef KEYBOARD_H
#define KEYBOARD_H

#include "config.h"

// ============================================================
// 按键事件类型
// ============================================================
enum KeyEvent {
  KEY_NONE = 0,
  KEY_PRESSED = 1,
  KEY_RELEASED = 2,
  KEY_HOLD = 3
};

// ============================================================
// 初始化键盘
// ============================================================
void keyboardInit();

// ============================================================
// 扫描键盘，返回基础层按键字符
// 如果没有按键按下，返回 '\0'
// ============================================================
char keyboardScan();

// ============================================================
// 获取当前按键事件（带消抖）
// ============================================================
KeyEvent getKeyEvent(char& keyChar);

// ============================================================
// 等待按键按下（阻塞）
// ============================================================
char waitForKey();

// ============================================================
// 根据FN层状态获取实际按键字符
// ============================================================
char getKeyForFnLayer(char baseKey, FnLayerState fnState);

// ============================================================
// 获取FN层状态名称（用于显示）
// ============================================================
const char* getFnLayerName(FnLayerState state);

#endif // KEYBOARD_H
